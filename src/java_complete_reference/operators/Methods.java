package java_complete_reference.operators;

public class Methods {
    public static double sum (double v1,double v2){
        return v1 + v2;
    }
    public static double sub (double v1,double v2){
        return v1 - v2;
    }
    public static double div (double v1,double v2){
        return v1 / v2;
    }
    public static double mul (double v1,double v2){
        return v1 * v2;
    }
    public static double mod (double v1,double v2){
        return v1 % v2;
    }
    public static double square (double v){
        return Math.sqrt(v);
    }
    public static double randNum (double end){
        return Math.random()*end;
    }
    public static double cosTheta (double v){
        return Math.cos(v);
    }
    public static double sinTheta (double v){
        return Math.sin(v);
    }
    public static double exponent (double v){
        return Math.exp(v);
    }
    public static double log (double v){
        return Math.log(v);
    }
    public static double log10 (double v){
        return Math.log10(v);
    }
    public static double log1p (double v){
        return Math.log1p(v);
    }
    public static double power (double v,int p){
        return Math.pow(v,p);
    }
    public static double tanTheta (double v){
        return Math.tan(v);
    }
    public static String decToBin (int decimal){
        return "a";
    }
}
