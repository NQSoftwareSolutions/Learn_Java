package java_complete_reference.java_overview;

class Variable {
    public static void main (String[] arg){
        int i;//declaration
        i = 10;//define variable value
        System.out.println(i);// print variable value

        int j = 15;// declare & define variable

        int k, l , m;// declare multiple variable in single statement;

        //define variable
        k = 1;
        l = 2;
        m = 3;

        System.out.println(k+" "+l+" "+m);// print multiple variable value

        k = l = m = 10;// assign common value to multiple varibles

        System.out.println(k+" "+l+" "+m);// check value which we assign recently

    }
}
